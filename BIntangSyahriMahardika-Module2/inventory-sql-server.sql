-- 1. Tabel Users
CREATE TABLE users (
    id_user INT PRIMARY KEY IDENTITY(1,1),
    username VARCHAR(50) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    full_name VARCHAR(100),
    role VARCHAR(20) NOT NULL CHECK (role IN ('Admin', 'Staff'))
);

-- 2. Tabel Categories
CREATE TABLE categories (
    id_category INT PRIMARY KEY IDENTITY(1,1),
    name_category VARCHAR(100) NOT NULL
);

-- 3. Tabel Products
CREATE TABLE products (
    id_product INT PRIMARY KEY IDENTITY(1,1),
    sku VARCHAR(20) UNIQUE NOT NULL,
    name_product VARCHAR(150) NOT NULL,
    image VARCHAR(255),
    description VARCHAR(255),
    id_category INT,
    price DECIMAL(15, 2) NOT NULL,
    stock INT NOT NULL DEFAULT 0,
    min_threshold INT DEFAULT 10,
    CONSTRAINT FK_Product_Category FOREIGN KEY (id_category) REFERENCES categories(id_category)
);

-- 4. Tabel Stock_Transactions
CREATE TABLE stock_transactions (
    id_transaction INT PRIMARY KEY IDENTITY(1,1),
    id_product INT,
    id_user INT,
    type VARCHAR(10) NOT NULL CHECK (type IN ('IN', 'OUT')), 
    qty INT NOT NULL,
    reference_no VARCHAR(50),        
    notes NVARCHAR(MAX), -- Menggantikan TEXT
    transaction_date DATETIME DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_Trans_Product FOREIGN KEY (id_product) REFERENCES products(id_product),
    CONSTRAINT FK_Trans_User FOREIGN KEY (id_user) REFERENCES users(id_user)
);

